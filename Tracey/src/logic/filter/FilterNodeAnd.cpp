//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
FilterNodeAnd::FilterNodeAnd(FilterNode* left, FilterNode* right) :
  left(left),
  right(right)
{
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
FilterNodeAnd::~FilterNodeAnd()
{
  delete left;
  delete right;
}


//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
bool FilterNodeAnd::evaluate()
{
  return left->evaluate() && right->evaluate();
}